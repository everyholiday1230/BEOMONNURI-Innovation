export function iconSvg(name, size = 24) {
  const body = ICONS[name];
  if (!body) return '';
  return `<svg ${VIEW} width="${size}" height="${size}" aria-hidden="true" focusable="false">${body}</svg>`;
}

/** data-icon 속성을 가진 모든 요소에 SVG 주입 */
export function mountIcons(root = document) {
  root.querySelectorAll('[data-icon]').forEach((el) => {
    if (el.querySelector('svg')) return; // 이미 주입됨
    const name = el.getAttribute('data-icon');
    const svg = iconSvg(name);
    if (svg) el.insertAdjacentHTML('afterbegin', svg);
  });
}
