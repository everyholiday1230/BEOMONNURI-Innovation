    s = I18N.read_text(encoding="utf-8")
    if JA:
        s = insert("ja: { flag", "zh: { flag", JA, s)
    if ZH:
        s = insert("zh: { flag", None, ZH, s)
    I18N.write_text(s, encoding="utf-8")
    print(f"inserted ja={len(JA)} zh={len(ZH)}")


